sub calc_median
{
	my (@values) = @_;
	
	@values = sort {$a<=>$b} @values;
	my ($median,$n_of_values) = (0.0,scalar(@values));
	
	if($n_of_values%2)
	{ 
		$median = $values[int($n_of_values/2)]; 
	}
	else
	{ 
		$median = ($values[$n_of_values/2] + $values[($n_of_values/2)-1]) / 2; 
	}

	return $median;
}

